function the_single_product_prices( $point_id, $category_type, $id, $region_id ) {
	$point_products = carbon_get_post_meta( $point_id, 'point_basis_products' );
	if ( $point_products ) {
		foreach ( $point_products as $point_product ) {
			$point_type      = $point_product['point_type'];
			$point_type_test = true;
			if ( $category_type ) {
				$point_type_test = $category_type == $point_type;
			}
			if ( $point_type_test ) {
				$product_id   = $point_product['product'][0]['id'];
				$product_test = $product_id == $id;
				if ( $product_test ) {
					$sub_test            = true;
					$currencies          = $point_product['currency'];
					$base_currency       = $point_product['base_currency'];
					$base_currency_rate  = get_currency_rate( $base_currency );
					$point_product_price = get_point_product_price( $point_product );
					$price_str           = get_formated_price( $point_product_price, $base_currency );
					echo "<div class='table-column' data-code='$base_currency'  data-price='$point_product_price'>$price_str</div>";
					if ( $currencies ) {
						$UAH = $point_product_price * $base_currency_rate;
						foreach ( $currencies as $code ) {
							if ( $code != $base_currency ) {
								$rate                 = get_currency_rate( $code );
								$_point_product_price = $UAH / $rate;
								$_price_str           = get_formated_price( $_point_product_price, $code );
								echo "<div class='table-column'  data-code='$code' data-price='$_point_product_price'>$_price_str</div>";
							}
						}
					} else {
						echo "<div  class='table-column'  data-price='0'>-</div>";
					}
				}
			}
		}
	}
	$point_products = carbon_get_post_meta( $point_id, 'point_products' );
	if ( $point_products ) {
		foreach ( $point_products as $point_product ) {
			$point_type      = $point_product['point_type'];
			$point_type_test = true;
			if ( $category_type ) {
				$point_type_test = $category_type == $point_type;
			}
			if ( $point_type_test ) {
				$_products = $point_product['products'];
				if ( $_products ) {
					foreach ( $_products as $product_id ) {
						$product_test = $product_id == $id;
						$basis        = $point_product['basis'];
						if ( $product_test && $basis && get_post( $basis ) ) {
							$sub_test            = true;
							$currencies          = $point_product['currency'];
							$price_data          = get_basis_product_price( $product_id, $basis, $point_type );
							$price               = $price_data['price'];
							$base_currency       = $price_data['base_currency'];
							$base_currency_rate  = get_currency_rate( $base_currency );
							$point_product_price = get_simple_point_product_price( $point_product, $product_id, $point_id, $region_id );
							$price_str           = get_formated_price( $point_product_price, $base_currency );
							echo "<div class='table-column' data-code='$base_currency'  data-price='$point_product_price'>$price_str</div>";
							if ( $currencies ) {
								$UAH = $point_product_price * $base_currency_rate;
								foreach ( $currencies as $code ) {
									if ( $code != $base_currency ) {
										$rate                 = get_currency_rate( $code );
										$_point_product_price = $UAH / $rate;
										$_price_str           = get_formated_price( $_point_product_price, $code );
										echo "<div class='table-column'  data-code='$code' data-price='$_point_product_price'>$_price_str</div>";
									}
								}
							} else {
								echo "<div  class='table-column'  data-price='0'>-</div>";
							}
						}
					}
				}
			}
		}
	}
