function get_currency_sting( $currency ) {
	$arr = array( 'UAH', 'UAHVAT' );
	if ( in_array( $currency, $arr ) ) {
		$currency = ' грн.';
	} elseif ( $currency == 'USD' ) {
		$currency = ' $';
	}

	return $currency;
}